static void renderLogo(void) {

    oled_write_raw_P(banana_logo2 , sizeof(banana_logo2));
}

bool oled_task_user(void) {
    // Host Keyboard Layer Status
    //togglePin(GP25);
    writePinHigh(GP25);
    switch (get_highest_layer(layer_state)) {
    case L_DEF:
        renderLogo();
        break;
    case L_CMPKT:
        oled_write_raw_P(banana_compact , sizeof(banana_compact));
        break;
    case L_F1:
        oled_write_raw_P(banana_f1layer , sizeof(banana_f1layer));
        break;
    case L_ARROW:
        oled_write_raw_P(banana_arrows , sizeof(banana_arrows));
        break;
    }
    //oled_write_P(PSTR("Layer: "), false);
    return false;
}

